    // Manipulação do formulário de demonstração
    document.addEventListener('DOMContentLoaded', function() {
        const demoForm = document.getElementById('demo-form');
        
        if (demoForm) {
            demoForm.addEventListener('submit', function(e) {
                e.preventDefault();
                
                // Preparar dados do formulário
                const formData = new FormData();
                formData.append('nome', document.getElementById('demo-name').value);
                formData.append('email', document.getElementById('demo-email').value);
                formData.append('empresa', document.getElementById('demo-company').value);
                formData.append('telefone', document.getElementById('demo-phone').value);
                formData.append('localizacao', document.getElementById('demo-reservoirs').value);
                formData.append('necessidades', document.getElementById('demo-message').value);
                
                // Desabilitar botão de envio
                const submitBtn = demoForm.querySelector('button[type="submit"]');
                const originalText = submitBtn.innerHTML;
                submitBtn.disabled = true;
                submitBtn.innerHTML = '<span class="material-icons-round">hourglass_empty</span> Enviando...';
                
                // Enviar dados
                fetch('send_demo.php', {
                    method: 'POST',
                    body: formData
                })
                .then(response => response.json())
                .then(data => {
                    if (data.success) {
                        // Sucesso
                        submitBtn.innerHTML = '<span class="material-icons-round">check_circle</span> Enviado!';
                        submitBtn.style.background = '#4caf50';
                        
                        // Mostrar mensagem de sucesso
                        alert('✅ ' + data.message);
                        
                        // Limpar formulário
                        demoForm.reset();
                        
                        // Restaurar botão após 3 segundos
                        setTimeout(() => {
                            submitBtn.disabled = false;
                            submitBtn.innerHTML = originalText;
                            submitBtn.style.background = '';
                        }, 3000);
                    } else {
                        // Erro
                        submitBtn.innerHTML = '<span class="material-icons-round">error</span> Erro';
                        submitBtn.style.background = '#f44336';
                        
                        // Mostrar mensagem de erro
                        alert('❌ ' + data.message);
                        
                        // Restaurar botão após 2 segundos
                        setTimeout(() => {
                            submitBtn.disabled = false;
                            submitBtn.innerHTML = originalText;
                            submitBtn.style.background = '';
                        }, 2000);
                    }
                })
                .catch(error => {
                    console.error('Erro:', error);
                    
                    submitBtn.innerHTML = '<span class="material-icons-round">error</span> Erro';
                    submitBtn.style.background = '#f44336';
                    
                    alert('❌ Erro ao enviar solicitação. Verifique sua conexão e tente novamente.');
                    
                    // Restaurar botão após 2 segundos
                    setTimeout(() => {
                        submitBtn.disabled = false;
                        submitBtn.innerHTML = originalText;
                        submitBtn.style.background = '';
                    }, 2000);
                });
            });
        }
    });
